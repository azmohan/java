/*
6、使用两个二维数组a和b存储两个3*4阶矩阵的元素值，
然后用两重for循环进行相加求值产 生二维数组c，最后输出c的各元素值。

*/
#include <stdio.h>

int main(void)
{
	int a[3][4]={{3,-2,1,2},{0,1,3,-2},{3,1,0,4}};
	int b[3][4]={{-2,3,0,-1},{1,0,-2,3},{-2,0,1,-3}};
	
	int i,j,c[3][4];

	for (i=0; i<3; i++)
	{
		for (j=0; j<4; j++)
		{
			c[i][j]=a[i][j]+b[i][j];
		}
	}

	for (i=0; i<3; i++)
	{
		for (j=0; j<4; j++)
		{
			printf("%d ", c[i][j]);
		}
		printf("\n");
	}


	return 0;

	
}